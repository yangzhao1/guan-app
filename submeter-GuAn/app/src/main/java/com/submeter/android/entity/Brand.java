public class Brand {

    /* 品牌id */
    private String id;

    /*品牌logo*/
    private String path;

    /*品牌名称*/
    private String title;

    /*店铺ID*/
    private String storeId;

    /*商品id*/
    private String productId;

    public String getId() {
        return id;
    }

    public String getPath() {
        return path;
    }

    public String getTitle() {
        return title;
    }

    public String getStoreId() {
        return storeId;
    }

    public String getProductId() {
        return productId;
    }
}
